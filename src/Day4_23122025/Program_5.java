package Day4_23122025;

public class Program_5 {
    public static void main(String[] args) {

        // print numbers from range 1111 to 1121
        System.out.println("Printing numbers from range 1111 to 1121");
        for (int i = 1111; i < 1122; i++) {
            System.out.print(i+" ");
        }
        System.out.println();

        // print numbers from range 1234 to 1223
        System.out.println("Printing numbers from range 1234 to 1223");
        for (int i = 1234; i > 1222; i--) {
            System.out.print(i+" ");
        }
        System.out.println();


        // print numbers from range -1111 to -1121
        System.out.println("Printing numbers from range -1111 to -1121");
        for (int i = -1111; i > -1122; i--) {
            System.out.print(i+" ");
        }
        System.out.println();

        // print numbers from range -1234 to -1223
        System.out.println("Printing numbers from range -1234 to -1223");
        for (int i = -1234; i < -1222; i++) {
            System.out.print(i+" ");
        }
        System.out.println();
    }
}
