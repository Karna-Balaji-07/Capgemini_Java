package Day4_23122025;


public class Program_1 {
    public static void main(String[] args) {
        // print numbers from the given range -123 to -129

        System.out.println("Print Numbers from the range -123 to -129");
        int i = -123;
        while(i > -130){
            System.out.print(i+" ");
            i--;
        }
        System.out.println();

        System.out.println("Print Numbers from the range 79 to 84");
        i = 79;
        while (i < 85){
            System.out.print(i+" ");
            i ++;
        }
        System.out.println();
        System.out.println("Print Numbers from the range -110 to -100");
        i = -110;
        while(i < -100){
            System.out.print(i+" ");
            i++;
        }
        System.out.println();
        System.out.println("Print Numbers from the range 0 to -9");
        i = 0;
        while(i > -10){
            System.out.print(i+" ");
            i--;
        }
    }
}

